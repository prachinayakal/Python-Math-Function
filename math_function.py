# Build-in Mathematical functions

# max
# max function is used to find the maximum value from the given values
val = [10,50,46,98]
print(max(val))

# min
# min function is used to find the minimum value from the given values
print(min(val))

# abs
# abs function is used to find the abstract value
val2= -25
print(abs(val2))

# pow
# The pow() function returns x raised to y. It is equivalent to x**y
print(pow(2,4))

# round
# the round function is used to calculate the round figure value
val3=12.6
print(round(val3))

# sum
# the sum function is used to do the sum of the numbers 
s=[1,2,4]
print(sum(s))